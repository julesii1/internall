def get_circle_perimeter(r):
  perimeter = 23.14*r
  print("Perimeter of Circle: ",perimeter)

def get_circle_area(r):
  area = 3.14*r*r
  print("Area of Circle: ",area)

def get_triangle_area(l,h):
  area=0.5*l*h
  print("Area of Triangle : ",area)


